class ForTest{
	public static void main(String args[]){
		int x;
		for(x=0; x<10; x=x+1) 
			System.out.println("This is x: "+x);
		//inplace of x=x+1, you can also use x++ to increement 1 to the value of x 
		//and x-- will decreement or subtract 1 from the value of x each time loop iterates.
	}
}